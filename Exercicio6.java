public class Exercicio6 {

    public static void main(String[] args) {

        int num1 = 10;
        int num2 = 2;
        char operacao = '*';

        if (operacao == '+') {
            System.out.println(num1 + num2);
        } else if (operacao == '-') {
            System.out.println(num1 - num2);
        } else if (operacao == '*') {
            System.out.println(num1 * num2);
        } else if (operacao == '/') {
            System.out.println(num1 / num2);
        }

    }

}
