public class Exercicio5 {

    public static void main(String[] args) {

        String nome = "Luna";
        double nota = 5.5;

        if (nota >= 6) {
            System.out.println(nome + " está aprovado");
        } else if (nota >= 4) {
            System.out.println(nome + " está de recuperação");
        } else {
            System.out.println(nome + " está reprovado");
        }

    }

}
